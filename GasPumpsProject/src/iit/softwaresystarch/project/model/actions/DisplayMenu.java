package iit.softwaresystarch.project.model.actions;

/**
 * The anstract Class DisplayMenu.
 * This Class is used as an Interface for the Strategy pattern.
 * The goal is to have multiple implemtation of the method displayMenu.
 */
public abstract class DisplayMenu {

  /**
   * Instantiates a new DisplayMenu.
   */
  public DisplayMenu() {}

  /**
   * Displays a menu.
   */
  public void displayMenu() {}

}
