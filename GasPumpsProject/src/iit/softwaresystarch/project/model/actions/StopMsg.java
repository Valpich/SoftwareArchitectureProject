package iit.softwaresystarch.project.model.actions;

/**
 * The abstract Class StopMsg.
 * This Class is used as an Interface for the Strategy pattern.
 * The goal is to have multiple implemtation of the method stopMsg.
 */
public abstract class StopMsg {

  /**
   * Instantiates a new StopMsg.
   */
  public StopMsg() {}

  /**
   * Displays a stop message.
   */
  public void stopMsg() {}

}
